function DeletePolygon(varargin)

%------------------------------------------------------------------------------
% Get gui data & axes handle
%------------------------------------------------------------------------------
gui = % guidata(varargin{1}); % Gui data

%------------------------------------------------------------------------------
% Get line handle
%------------------------------------------------------------------------------
handle = gco;

%------------------------------------------------------------------------------
% Get user data, (location of constraint
%------------------------------------------------------------------------------
x = get(handle,'Xdata')';
y = get(handle,'Ydata')';

%------------------------------------------------------------------------------
% Remove from plot
%------------------------------------------------------------------------------
delete(handle)

for i = 1:length(gui.PTS.Poly)
    
    if length(gui.PTS.Poly(i).x) == length(x)
        
        if isequal(gui.PTS.Poly(i).x,x) && isequal(gui.PTS.Poly(i).y,y)
            
            break;
            
        end
        
    end
    
end

gui.PTS.Poly(i) = [];

