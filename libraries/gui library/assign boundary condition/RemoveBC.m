function RemoveBC(varargin)

%------------------------------------------------------------------------------
% Get gui data & axes handle
%------------------------------------------------------------------------------
gui = % guidata(varargin{1}); % Gui data

%------------------------------------------------------------------------------
% Get line handle
%------------------------------------------------------------------------------
handle = gco;

%------------------------------------------------------------------------------
% Get user data, (location of constraint
%------------------------------------------------------------------------------
x = get(handle,'Xdata')';
y = get(handle,'Ydata')';
%loc = get(handle,'UserData');

%------------------------------------------------------------------------------
% Remove from plot
%------------------------------------------------------------------------------
delete(handle)

%------------------------------------------------------------------------------
% Remove from PTS data structure
%------------------------------------------------------------------------------

for i = 1:length(gui.PTS.Constraints)
    
    if size(gui.PTS.Constraints(i).xy,1) == length(x)
        
        if isequal(gui.PTS.Constraints(i).xy(:,1),x) && isequal(gui.PTS.Constraints(i).xy(:,2),y)
            
            break;
            
        end
        
    end
    
end

gui.PTS.Constraints(i) = [];

